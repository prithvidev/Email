    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String first = fname.getText();
        String last = lname.getText();
        String phone = phn.getText();
        String email= em.getText();
        String password = ps.getText();
        String country = c.getText();
        String address = add.getText();
        String city = ci.getText();
        String postal = pc.getText();
        if(first.isEmpty() || email.isEmpty() || password.isEmpty() || phone.isEmpty() || last.isEmpty() || country.isEmpty() || address.isEmpty() || city.isEmpty() || postal.isEmpty())
        {JOptionPane.showMessageDialog(null,"NO DATA ENETERED");}
            else{
        try{
            Connection con;
            myconnection registercon = new myconnection();
            con = registercon.getRegisterConnection();
            String sql = "select * from details where email='"+email+"'&& Password='"+password+"'";
            PreparedStatement p = con.prepareStatement(sql);
            ResultSet rs = p.executeQuery(sql);
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "ALREADY REGISTERED");
                 fname.setText("");
                 lname.setText("");
                 phn.setText("");
                 em.setText("");
                 ps.setText("");
                 c.setText("");
                 add.setText("");
                 ci.setText("");
                 pc.setText("");
                 error.setText("");
                 error1.setText("");
                 
            }
            else{
            String query = "insert into details(first_name,last_name,phno,email,Password,country,Address,City,PostalCode)values(?,?,?,?,?,?,?,?,?)";
            PreparedStatement p1 = con.prepareStatement(query);
            
            p1.setString(1,first);
            p1.setString(2, last);
            p1.setString(3, phone);
            p1.setString(4, email);
            p1.setString(5, password);
            p1.setString(6, country);
            p1.setString(7, address);
            p1.setString(8, city);
            p1.setString(9, postal);
            
            p1.executeUpdate();
            JOptionPane.showMessageDialog(null,"USER REGISTERED SUCCESSFULLY");
            }
            
        }
        catch(SQLException ex){
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

